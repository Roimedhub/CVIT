'use client'

import { useEffect, useState } from 'react'

interface Cell { x: number; y: number; key: string }

export default function BackgroundPattern() {
  const [cells, setCells] = useState<Cell[]>([])

  useEffect(() => {
    const COLS = 3
    const M_W = Math.floor(window.innerWidth / COLS)
    const M_H = Math.round(M_W * 0.55)     // keeps M proportions
    const ROWS = Math.ceil(window.innerHeight / M_H) + 2

    const result: Cell[] = []
    for (let row = 0; row < ROWS; row++) {
      for (let col = 0; col < COLS + 1; col++) {
        const xShift = row % 2 === 0 ? 0 : M_W / 2
        result.push({
          x: col * M_W + xShift - M_W / 2,
          y: row * M_H - M_H / 2,
          key: `${row}-${col}`,
        })
      }
    }
    setCells(result)
  }, [])

  return (
    <div
      style={{
        position: 'absolute',
        inset: 0,
        overflow: 'hidden',
        pointerEvents: 'none',
        zIndex: 0,
      }}
    >
      {cells.map(({ x, y, key }) => (
        <svg
          key={key}
          viewBox="0 0 76 42"
          xmlns="http://www.w3.org/2000/svg"
          style={{
            position: 'absolute',
            left: x,
            top: y,
            width: `${100 / 3}vw`,
            opacity: 0.35,
          }}
        >
          <path
            fillRule="evenodd"
            clipRule="evenodd"
            d="M46.4145 8.68829C46.9575 8.68829 47.9621 8.82404 48.8309 9.69287L65.9903 26.8522C67.3207 28.1826 67.3207 30.3819 65.9903 31.7122C65.1214 32.5811 64.0897 32.7168 63.5738 32.7168C63.0308 32.7168 62.0262 32.5811 61.1574 31.7122L52.5777 23.1326L46.4416 16.9965L40.3055 23.1326L37.8891 25.549L35.4727 23.1326L29.3366 16.9965L23.119 23.1326L14.5393 31.7122C13.6705 32.5811 12.6387 32.7168 12.1229 32.7168C11.5799 32.7168 10.5753 32.5811 9.70644 31.7122C8.37605 30.3819 8.37605 28.1826 9.70644 26.8522L26.8658 9.69287C27.7346 8.82404 28.7664 8.68829 29.2822 8.68829C29.8253 8.68829 30.8298 8.82405 31.6987 9.69287L37.8348 15.829L43.9709 9.69287C44.8669 8.82404 45.8986 8.68829 46.4145 8.68829M46.4145 0C43.3193 0 40.1969 1.19464 37.8348 3.55677C35.4727 1.16749 32.3775 0 29.2822 0C26.1599 0 23.0647 1.16749 20.7026 3.55677L3.54319 20.689C-1.18106 25.4132 -1.18106 33.0969 3.54319 37.8484C5.90532 40.2105 9.02767 41.4051 12.1229 41.4051C15.2181 41.4051 18.3404 40.2105 20.7026 37.8484L29.2823 29.2687L35.9071 35.9206C36.4501 36.4637 37.1289 36.708 37.8348 36.708C38.5407 36.708 39.2195 36.4365 39.7625 35.9206L46.4145 29.2687L54.9942 37.8484C57.3563 40.2105 60.4787 41.4051 63.5739 41.4051C66.6691 41.4051 69.7914 40.2105 72.1535 37.8484C76.8778 33.1241 76.8778 25.4404 72.1535 20.689L54.9942 3.52962C52.632 1.16749 49.5368 0 46.4145 0Z"
            fill="#1a1a90"
          />
          <path
            fillRule="evenodd"
            clipRule="evenodd"
            d="M55.2659 17.783L48.0166 10.5337C47.1477 9.66487 45.7359 9.66487 44.8399 10.5337L37.835 17.5386L30.8301 10.5337C29.9612 9.66487 28.5494 9.66487 27.6534 10.5337L20.4312 17.8101C19.6439 18.5975 19.5081 19.8736 20.1326 20.7424C20.5127 21.2855 21.11 21.6113 21.7616 21.6656C22.4133 21.7199 23.0649 21.4755 23.5265 21.0139L29.2824 15.2851L37.8621 23.8648L46.4147 15.2851L52.1435 21.0139C52.6051 21.4755 53.2567 21.7199 53.9083 21.6656C54.5599 21.6113 55.1573 21.2854 55.5374 20.7424C56.189 19.8465 56.0532 18.5975 55.2659 17.783Z"
            fill="#16168a"
          />
        </svg>
      ))}
    </div>
  )
}
